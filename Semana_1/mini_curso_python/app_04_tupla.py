# Exemplo de tupla
coordenadas = (25.9, 34.6)
#  Índice     (   0,    1)

print(coordenadas[1])
print(coordenadas[0])

# Tuplas são imutáveis, ou seja, não podem ser alteradas após a criação
# coordenadas[0] = 30.0  # Isso causaria um erro
# Tuplas podem ser usadas para armazenar dados que não precisam ser alterados
# Exemplo: coordenadas = (25.9, 34.6)
# Tuplas são definidas usando parênteses ()
# Exemplo: tupla_exemplo = (1, 2, 3)
# Acessando elementos de uma tupla
# Podemos acessar elementos de uma tupla usando o índice, que começa em 0
# Exemplo: coordenadas[0] retorna 25.9
